	BREQ VERIF_NOV
	CPI R27, 4
	BREQ MESES_30_CONFIRMADO
	CPI R27, 6
	BREQ MESES_30_CONFIRMADO
	CPI R27, 9
	BREQ VERIF_SEP	
	RET

VERIF_SEP:
	LDI R27, 0XFF //LE ASIGNAMOS ESE VALOR PARA QUE AL MOMENTO DE INCREMENTAR R27 LLEGE A 0
	INC R28
	RJMP MESES_30_CONFIRMADO

VERIF_NOV:
	CPI R28, 1
	BREQ MESES_30_CONFIRMADO
	RET

MESES_30_CONFIRMADO:
	LDI R25, 1 //LE ASIGNAMOS 1 PARA QUE EMPIECE DESDE PRIMERO 
	INC R27
	CLR R26
	RET

VERIF_31:
