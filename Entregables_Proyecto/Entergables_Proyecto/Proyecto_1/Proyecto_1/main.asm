	CPI R25, 2
	BREQ VERIF_31
	CPI R25, 9
	BREQ VERIF_FEB
	CPI R25, 10
	BREQ VERIF_10_DIAS
	RET

//REALIZAMOS LAS RESPECTIVAS VERIFICACIONES 
VERIF_10_DIAS:
