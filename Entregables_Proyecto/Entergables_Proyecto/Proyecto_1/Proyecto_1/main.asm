/****************RUTINAS MODO 1******************/
DIES_MIN:
	CLR R2
	RJMP DIES_MINUTOS_2

DIES_MINUTOS_2:
	CLR R24
	INC R19
	CPI R19, 0x06 //SI LLEGA A 6 ES QUE YA HAY 60 MIN = 1 HORA
	BREQ UNA_HORA
	RET

UNA_HORA:
	CLR R19
	INC R18
	CPI R18, 0x04 //HACE LA COMPARACIÓN CON 4 PARA VERIFICAR SI YA ES UN DIA
	BREQ VERIF_DIA
	CPI R18, 0x0A
	BREQ DIES_HORA
	RET

VERIF_DIA:
	CPI R16, 2
	BREQ UN_DIA //SI YA VERIFICAMOS QUE ES 24 EN LAS HORAS, INCREMENTAMOS UN DÍA. 
	RET

DIES_HORA:
