/****************CONFIGURACIONES DE LAS INTERRUPCIONES************************/
TIMER1_SETUP:
	LDI R16, 0x00
	STS TCCR1A, R16 //ACTIVAMOS LA CONFIGURACION
	LDI R16, (1 << CS12)|(1 << CS10)
	STS TCCR1B, R16 //INDICAMOS EL PRESCALADO QUE QUEREMOS (1024)
	LDI R16, HIGH(T1_VALOR)
	STS TCNT1H, R16
	LDI R16, LOW(T1_VALOR)
	STS TCNT1L, R16 //CARGAMOS EL VALOR INICIAL
	LDI R16, (1 << TOIE1)
	STS TIMSK1, R16
	RET

TIMER0_SETUP:
	LDI R16, 0x00
	OUT TCCR0A, R16	//ACTIVAMOS LA CONFIGURACION
	LDI R16, (1 << CS02)|(1 << CS00)
	OUT TCCR0B, R16	//INDICAMOS EL PRESCALADO QUE QUEREMOS (1024)
	LDI R16, 12
	OUT TCNT0, R16	//CARGAMOS EL VALOR INICIAL
	LDI R16, (1 << TOIE0)
	STS TIMSK0, R16
	RET

PIN_CHANGE_SETUP:
	LDI R16, (1 << PCIE1) //HABILITAMOS PIN CHANGE EN EL PUERTO C
	STS PCICR, R16
	LDI R16, (1 << PCINT10)|(1 << PCINT11)|(1 << PCINT12)|(1 << PCINT13) //HABILITAMOS LA INTERRUPCIÓN A LOS BITS 2, 3, 4 Y 5
	STS PCMSK1, R16
	RET

