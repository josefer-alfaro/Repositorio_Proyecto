	CBI PORTD, PORTD3
	SBI DDRD, DDD4
	CBI PORTD, PORTD4
	SBI DDRD, DDD5
	CBI PORTD, PORTD5
	SBI DDRD, DDD6
	CBI PORTD, PORTD6
	SBI DDRD, DDD7
	CBI PORTD, PORTD7

	//MULTIPLEXEO
	SBI DDRB, DDB0
	SBI DDRB, DDB1
	SBI DDRB, DDB2
	SBI DDRB, DDB3
	CBI PORTB, PORTB0
	CBI PORTB, PORTB1
	CBI PORTB, PORTB2
	CBI PORTB, PORTB3

	//LEDS
	SBI DDRB, DDB4
	SBI DDRB, DDB5
	SBI DDRC, DDC0
	CBI PORTB, PORTB4
	CBI PORTB, PORTB5
	CBI PORTC, PORTC0
	SBI DDRC, DDC1
	CBI PORTC, PORTC0

	//DEFINIMOS LAS SALIDAS PARA LAS INTERRUPCIONES PIN_CHANGE
	CBI DDRC, DDC2
	CBI DDRC, DDC3
	CBI DDRC, DDC4
	CBI DDRC, DDC5
	SBI PORTC, PORTC2
	SBI PORTC, PORTC3
	SBI PORTC, PORTC4
	SBI PORTC, PORTC5

	//LIMPIAMOS REGISTROS Y DESIGNAMOS VALORES
	CLR MODO
	CLR R21
	CLR R22
	CLR R1
	CLR R2
	IN R16, PINC
	ANDI R16, 0b00111100
	MOV R3, R16
	CLR R6
	CLR R7
	CLR R8
	CLR R9
	CLR R10
	CLR R12
	CLR R16
	CLR R17
	CLR R18
	CLR R19
	CLR R24
	LDI R25, 0x01
	CLR R26
	LDI R27, 0x01
	CLR R28
	CLR R29
	CLR R30
	CLR R31

	LDI R16, 2
	STS ALARMA_PRIMERO_HORA, R16
	STS ALARMA_SEGUNDO_HORA, R16
	STS ALARMA_PRIMERO_MIN, R16
	STS ALARMA_SEGUNDO_MIN, R16
	CLR R16
	STS CONTADOR_TIMER0, R16

	CLR R16
	SEI
/****************************************/
// Loop Infinito
MAIN_LOOP:
	CALL CHEQUEO_ALARMA //LLAMA A LA FUNCIÓN PARA VER SI LA ALARMA COINCIDE CON LA HORA
	PUSH R28
	LDI R28, 0x01
	CP R28, R2
	BREQ OVERFLOW_HORA //LLAMA LA FUNCION PARA INDICAR QUE YA PASO 10 MIN
	POP R28 
	CPI MODO, 2					//AQUI ESTAN LOS BRANCHES PARA TODAS LA FUNCIONES DEL RELOJ
	BREQ LLAMADO_SETUP_HORA
	CPI MODO, 3
	BREQ LLAMADO_SETUP_MIN
	CPI MODO, 4
	BREQ LLAMADO_SETUP_MES
	CPI MODO, 5
	BREQ LLAMADO_SETUP_DIA
	CPI MODO, 6
	BREQ LLAMADO_SETUP_ALARMA_HORA
	CPI MODO, 7
	BREQ LLAMADO_SETUP_ALARMA_MIN
	CALL COPIA						
	CPI MODO, 0					//SE COLOCA DESPUES DE LA FUNCION COPIA PARA QUE NO SOBRE ESCRIBA LOS VALORES EN LA CONFIGURACION
	BREQ LLAMADO_HORA
	CPI MODO, 1
	BREQ LLAMADO_FECHA
	RJMP MAIN_LOOP

OVERFLOW_HORA: //PRIMERA PARTE DEL MODO 0
	POP R28
	CALL DIES_MIN
	RJMP MAIN_LOOP

