	INC R16
	RET

/**************************************RUTINAS MODO 2********************************************/
UN_DIA:
	CLR R16
	CLR R18
	INC R25
	CPI R25, 1 // VERIFICAMOS TODOS LAS POSIBILIDADES EN EL CAMBIO DE UNIDADES DE DIA
