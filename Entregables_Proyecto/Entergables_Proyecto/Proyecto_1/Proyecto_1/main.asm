/************************/
 PIN_CHANGE_ISR:
	PUSH R27
	IN R27, PINC
	ANDI R27, 0b00111100
	MOV R5, R27
	EOR R5, R3 //PARA VER QUE PINES CAMBIARON DE VALOR
	SBRS R5, 5 //PARA VER SI CAMBIÓ EL DE CONFIRMACIÓN 
	RJMP REVISAR_MODO
	SBRC R27, 5
	RJMP REVISAR_MODO
	SBRS R29, 0 //SALTA LA SIGUENTE LINEA SI NO SE A PRESIONADO EL BOTON CONFIRMACIÓN 
	RJMP REVISAR_MODO
	SBR R29, (1<<1) //ACTIVA LA FLAG PARA EL "SILENCIADO"
	CBI PORTC, PORTC0
	INC R8 //ACTIVA LA FLAG DEL BOTON
	RJMP OUT_PIN_CHANGE_ISR

REVISAR_MODO:
	SBRS R5, 2 //AQUÍ VA REVISANDO MODO POR MODO PARA VER EN DONDE FUE EL CAMBIO
	RJMP OTRO_MODO
	SBRC R27, 2
	RJMP OUT_PIN_CHANGE_ISR
	INC MODO
	CPI MODO, 8
	BREQ OVERFLOW_MODOS //OVERFLOW PARA LOS MODOS
	RJMP OUT_PIN_CHANGE_ISR

OVERFLOW_MODOS:
	CLR MODO
	RJMP OUT_PIN_CHANGE_ISR

OTRO_MODO: //OTRO MODO APARTE DE LOS PRIMEROS 2
	MOV R3, R27
	CPI MODO, 0
	BREQ OUT_PIN_CHANGE_ISR
	CPI MODO, 1
	BREQ OUT_PIN_CHANGE_ISR
	RJMP SUBIR

SUBIR: //VERIFICAR SI HUBO CAMBIO EN LOS BOTONES DE SUBIDA O BAJADA
	SBRS R5, 3
	RJMP BAJAR
	SBRS R27, 3
	RJMP OUT_PIN_CHANGE_ISR
	INC R6 //ACTIVA FLAG DE SUBIDA
	RJMP OUT_PIN_CHANGE_ISR

BAJAR: 
	SBRS R5, 4
	RJMP CONFIRMACION
	SBRS R27, 4
	RJMP OUT_PIN_CHANGE_ISR
	INC R7 //ACTIVA LA FLAG DE BAJADA
	RJMP OUT_PIN_CHANGE_ISR

CONFIRMACION:
	SBRS R5, 5
