.include "M328PDEF.inc"     // Include definitions specific to ATMega328P
.dseg
.org    SRAM_START
.equ T1_VALOR			= 0x1B1E
.def MODO				= R20		

//variable_name:     .byte   1   // Memory alocation for variable_name:     .byte   (byte size)

//COLOCAMOS VECTORES DE INTERRUPCIONES
.cseg
.org 0x0000
	RJMP START
.org PCI1addr
	RJMP PIN_CHANGE_ISR
.org OVF1addr
	RJMP TIMER1_ISR
.org OVF0addr
	RJMP TIMER0_ISR

START:
 /****************************************/
// Configuración de la pila 
LDI     R16, LOW(RAMEND)
OUT     SPL, R16
LDI     R16, HIGH(RAMEND)
OUT     SPH, R16
/****************************************/
//DESIGNAMOS REGISTROS EN LA RAM
 .dseg
 COPIA_SEGUNDO_MIN: .byte 1
 COPIA_PRIMERO_MIN: .byte 1
 COPIA_SEGUNDO_HORA: .byte 1
 COPIA_PRIMERO_HORA: .byte 1
 COPIA_SEGUNDO_DIA: .byte 1
 COPIA_PRIMERO_DIA: .byte 1
